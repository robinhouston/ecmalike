// Compute the score for a single parameter value
double score(struct alg *alg, uint64_t mask) {
    int score = 0;

    for (int i = 0; i < N_PRIMES; ++i) {
        uint64_t p = random_odd_prime(mask);
        // printf("Using prime %llu:\n", p);
        struct runtime *runtime = newRuntime(alg, p);

        for (uint64_t *p = &FACTORS[0]; *p; ++p) {
            runtime_power(runtime, *p);
        }

        uint64_t value = runtime_value(runtime);
        // printf("    value = %llu\n", value);
        if (value == 0) score += 1;

        destroyRuntime(runtime);
    }

    return (double) score / N_PRIMES;
}

double multiscore(struct alg *alg, uint64_t mask) {
    double s = 0;
    for (int i = 0; i < N_PARAMETER_VALUES; ++i) {
        s += score(alg, mask);
    }
    return s / N_PARAMETER_VALUES;
}

